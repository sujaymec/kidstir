				<h4 class="diy-green-heading">For a fancy French breakfast, serve up some crepes! 
					The batter for these thin pancakes can be mixed up in a blender in a flash. 
					Roll them up with fresh berries and sprinkle with confectioners' sugar.
				</h4>

				<h4 class="diy-green-heading">Ingredients</h4>

				<ul>

					<li>2 cups milk</li>
					<li>4 eggs</li>
					<li>3 tablespoons butter, melted and cooled</li>
					<li>1 tablespoon sugar</li>
					<li>1 teaspoon vanilla extract</li>
					<li>1/4 teaspoon salt</li>
					<li>1 1/2 cups flour</li>
					<li>confectioners' sugar</li>
					<li>maple syrup (optional)</li>

				</ul>


				<h4 class="diy-green-heading">Step-by-step</h4>

				<ol>

					<li>Place the milk, eggs, melted butter, sugar, vanilla, salt, and flour into a blender. Blend until the batter is smooth.</li>
					<li>Melt a small of pat of butter in a frying pan over medium-low heat.</li>
					<li>Pour about 1/4 cup batter into the pan. Turn the pan from side to side to make an even circle.</li>
					<li>Cook the crepe for about 1 to 2 minutes, or until lightly browned. Then flip it over with a spatula and cook the other side for 1 to 2 more minutes. Continue making the crepes until all the batter is used up.</li>
					<li>Place a crepe on a plate and add a handful of berries to the center. Roll it up and sprinkle with a little confectioners' sugar. Drizzle with a little maple syrup, if you'd like.</li>
				
				</ol>			



